/* c  program to print student details*/
#include<stdio.h>
void main()
{
	//variable declaration
	int age;
	float cgpa;
	char initial;
	
	// Initialization
	age = 19;
	cgpa = 6.7;
	initial = 'B';
	
	//output
	printf("age of student:%i\n",age);
	printf("cgpa of student:%f\n",cgpa);
	printf("initial of student:%c",initial);
}
